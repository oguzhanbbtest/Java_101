public class Main {
    public static void main(String[] args){
        int year = 2021;
        // do-while döngüsünde do kod bloğunun içinde ki ifade birkez çalışır ve
        // while koşuluna göre devam eder yada durur.
        do {
            System.out.println(year);
            year++;
        }
        while (year<2021);

    }


}
